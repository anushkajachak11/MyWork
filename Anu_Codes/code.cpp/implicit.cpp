#include<iostream>
using namespace std;

int main(){

    //Implicit Function
    cout<< (10/2) <<endl; //3
    cout<< (10/2.0) <<endl; //3.333

    cout<<('A'+1)<<endl;
    cout<<('B'+1)<<endl;
    cout<<('C'+1)<<endl;
    cout<<('A'-0)<<endl;
    cout<<('a'+2)<<endl;
    cout<<('b'+1)<<endl;
    
    return 0;

}
